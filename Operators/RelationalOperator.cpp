#include<iostream>
using namespace std;
int main(){
    //Relation Operator is ==, !=, >, <, <=, >=
    int a, b;
    cout<<"Enter two number: ";
    cin>>a>>b;
    cout<<"Is Equal To Operator: "<<(a==b)<<endl;
    cout<<"Not Equal To Operator: "<<(a!=b)<<endl;
    cout<<"Greater Than Operator: "<<(a>b)<<endl;
    cout<<"Less Than Operator: "<<(a<b)<<endl;
    cout<<"Less Thank Equal To Operator: "<<(a<=b)<<endl;
    cout<<"Greater Thank Equal To Operator: "<<(a>=b)<<endl;


}